package com.vo;

public class Author {
	private int person_id;
	private int paper_number;
	private String is_contact;
	public int getPerson_id() {
		return person_id;
	}
	public void setPerson_id(int person_id) {
		this.person_id = person_id;
	}
	public int getPaper_number() {
		return paper_number;
	}
	public void setPaper_number(int paper_number) {
		this.paper_number = paper_number;
	}
	public String getIs_contact() {
		return is_contact;
	}
	public void setIs_contact(String is_contact) {
		this.is_contact = is_contact;
	}
}
